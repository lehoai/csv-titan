package lehoai.csvtitan.service.core;

/**
 * Enum representing the data types of data stored in a CSV column.
 */
public enum Type {
    /**
     * Represents an integer type.
     */
    INT,

    /**
     * Represents a double-precision floating-point number type.
     */
    DOUBLE,

    /**
     * Represents a boolean type (true/false values).
     */
    BOOLEAN,

    /**
     * Represents a date type.
     */
    DATE,

    /**
     * Represents a string type (text).
     */
    STRING
}

